sub get_pnames {
	my @p = grep { $_ ne '' } @{ $Pnames };
	my @pn = grep { $_ ne '' } @{ $Pnames_exception };    # NG
	
	my @name;
	for my $tag(@_){
		if( $tag =~ /(P|Ｐ)$/io ){
			if( not first { $tag =~ /^$_$/i } @pn ){
				push @name, $tag;
			}
		}
		elsif( first { $tag eq $_ } @p ){
			push @name, $tag;
		}
	}
	
	return @name;
}

