sub get_pnames {
	my @p = grep { $_ ne '' } @{ $Pnames };
	my @pn = qw(MikuPOP RinPOP VochnoPop アニメOP ゲームOP エロゲOP 偽OP J-POP これからもずっとbakerの嫁P 2STEP ミーム第２期OP Human_Dump お前はもう死んでいるP);    # NG
	
	my @name;
	for my $tag(@_){
		if( $tag =~ /(P|Ｐ)$/io ){
			if( not first { $tag =~ /^$_$/i } @pn ){
				push @name, $tag;
			}
		}
		elsif( first { $tag eq $_ } @p ){
			push @name, $tag;
		}
	}
	
	return @name;
}

